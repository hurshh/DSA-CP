import java.math.BigInteger;
import java.util.Scanner;

public class bigint {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        BigInteger b1 = sc.nextBigInteger();
        System.out.println(b1);
        long l1 = sc.nextLong();
        System.out.println(l1);
    }
}
